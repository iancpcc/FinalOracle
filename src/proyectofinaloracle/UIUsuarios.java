    private void jTablePrivMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePrivMouseClicked
        if (cambiadoTablePriv) {

            int rowI = jTablePriv.rowAtPoint(evt.getPoint());
            PrivObj priv = new PrivObj(jTablePriv.getValueAt(rowI, 0).toString(), (Boolean) jTablePriv.getValueAt(rowI, 1));
            boolean existe = false;
            for (PrivObj listPriv : listPrivs) {
                if (listPriv.privilegio.equals(priv.privilegio)) {
                    listPriv.valor = priv.valor;
                    existe = true;
                    return;
                }
            }
            if (!existe) {

                listPrivs.add(priv);
            }

        }
    }//GEN-LAST:event_jTablePrivMouseClicked

