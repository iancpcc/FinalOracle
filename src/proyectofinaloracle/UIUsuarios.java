    private void BtnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearActionPerformed
       String usuario = txtUsuario.getText();
        query = new Consultas();
        try {
            if (BtnCrear.getText().equals("Crear")) { //Crear Usuario
                JOptionPane.showMessageDialog(null, "Usuario Creado", "SQL", JOptionPane.INFORMATION_MESSAGE);

                String contrasenia = txtContra.getText();
                if (query.crearUsuario(usuario, contrasenia)) {
                    if (listRoles.size() != 0) {
                        int rs = query.crearEditarRolesxUsuario(listRoles, usuario);
                        if (rs != 0) {
                            JOptionPane.showMessageDialog(null, "Algunos roles  no se puedieron asignar", "SQL", JOptionPane.INFORMATION_MESSAGE);
                        }
                    }
                    if (listPrivs.size() != 0) {
                        int rs2 = query.crearEditarPrivilegiosxUsuario(listPrivs, usuario);
                        if (rs2 != 0) {
                            JOptionPane.showMessageDialog(null, "Algunos  privilegios no se puedieron asignar", "SQL", JOptionPane.INFORMATION_MESSAGE);

                        }
                    }

                }

            } else { //Actualizar Usuario
                if (!txtContra.getText().isEmpty()) {
                    query.actualizarUsuario(txtUsuario.getText(), txtContra.getText());
                }

                if (listRoles.size() != 0 || listPrivs.size() != 0) {
                    int rs = query.crearEditarRolesxUsuario(listRoles, usuario);
                    int rs2 = query.crearEditarPrivilegiosxUsuario(listPrivs, usuario);
                    if (rs == 0 || rs2 == 0) {
                        JOptionPane.showMessageDialog(null, "Cambios Actualizados", "SQL", JOptionPane.INFORMATION_MESSAGE);
                        Arbol.setModel(tree.crearNodosxNombre(or));
                        this.dispose();
                        return;
                    }
                }
                JOptionPane.showMessageDialog(null, "Cambios Actualizados", "SQL", JOptionPane.INFORMATION_MESSAGE);
            }
            Arbol.setModel(tree.crearNodosxNombre(or));
            this.dispose();//Cierro la ventana

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex, "SQL", JOptionPane.INFORMATION_MESSAGE);
            listPrivs.clear();
            listRoles.clear();
            if (accion.equals("editar")) {//Cuando se actualiza un usuario
                txtUsuario.setText(nomUsuario);
                tabla.visualizarEditar(jTblRoles, "Roles", txtUsuario.getText());
                tabla.visualizarEditar(jTablePriv, "Privilegios", txtUsuario.getText());
            } else {//Cuando se crea un nuevo usuario
                tabla.visualizar(jTblRoles, "Roles");
                tabla.visualizar(jTablePriv, "Privilegios");
            }
        } catch (Exception ex) {
            Logger.getLogger(UIUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnCrearActionPerformed

