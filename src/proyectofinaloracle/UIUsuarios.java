    private void jTblRolesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblRolesMouseClicked
        if (cambiadoTableRoles) {
            int rowI = jTblRoles.rowAtPoint(evt.getPoint());
            RolesObj rol = new RolesObj(jTblRoles.getValueAt(rowI, 0).toString(), (Boolean) jTblRoles.getValueAt(rowI, 1));
            boolean existe = false;
            for (RolesObj listRole : listRoles) {
                if (listRole.rol.equals(rol.rol)) {
                    listRole.valor = rol.valor;
                    existe = true;
                    return;
                }
            }
            if (!existe) {
                listRoles.add(rol);
            }
        }

    }//GEN-LAST:event_jTblRolesMouseClicked
